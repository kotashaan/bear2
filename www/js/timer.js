////////////////////////////////////////////////////////////////////////////////
/// timers 動作サンプル everyTime stopTime
////////////////////////////////////////////////////////////////////////////////

var i=0;
var lim=30;
$(document).everyTime(1000,'timer01',function(){
	i++;
	if(i>=lim) $(document).stopTime('timer01');
	$('.sample01').text(i);
});


////////////////////////////////////////////////////////////////////////////////
/// timers 動作サンプル oneTime
////////////////////////////////////////////////////////////////////////////////

$(document).oneTime(5000,function(){
	$('.sample02').text('ポンっ！');
});








